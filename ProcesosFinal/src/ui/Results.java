    public void showValues(int pcore,LinkedList presult,ArrayList<String[]> pprocesos){
        core =pcore;
        result = presult;
        procesos = pprocesos;
        jLabel3.setText("Results Core "+core);
        DefaultTableModel modelCore = (DefaultTableModel) tableResults.getModel();
        float trts = 0,turntotal = 0;
        for(int i = 0; i < procesos.size();i++){
            ArrayList<String> array = new ArrayList<String>();
            array.add(procesos.get(i)[0]);
            GChart temp = (GChart) result.get(i);
            array.add(temp.ctime+"");
            array.add(temp.ttime+"");
            array.add((temp.ttime/Integer.parseInt(procesos.get(i)[1]))+"");
            trts += temp.ttime/Integer.parseInt(procesos.get(i)[1]);
            turntotal += temp.ttime;
            modelCore.addRow(array.toArray());
        }
        trts = trts/procesos.size();
        turntotal = turntotal/procesos.size();
        jLabel5.setText(trts+"");
        jLabel4.setText(turntotal+"");
    }


